package spil;

public class Player {

    private static String playerName;
    private static int playerPoint;



    public void setPlayerName(String name){
        playerName = name;
    }
    public String getPlayerName(){
        return playerName;
    }
    public void setPlayerPoint(int point){
        playerPoint = point;
    }
    public int getPlayerPoint(){
        return playerPoint;
    }


}
